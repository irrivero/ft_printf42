#include "ft_printf.h"

void	ft_putnbr(int n, int *d)
{
	if (n == -2147483648)
	{
		ft_putstr("-2147483648", d);
		return ;
	}
	if (n < 0)
	{
		n *= -1;
		ft_putchar('-', d);
	}
	if (n >= 10)
		ft_putnbr(n / 10, d);
	ft_putchar(n % 10 + 48, d);
}
