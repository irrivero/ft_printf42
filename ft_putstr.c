#include "ft_printf.h"

void	ft_putstr(const char *s, int *d)
{
	int	i;

	if (!s)
		*d += write(1, "(null)", 6);
	else
	{
		i = 0;
		while (s[i])
			ft_putchar(s[i++], d);
	}
}