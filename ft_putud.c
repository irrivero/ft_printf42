#include "ft_printf.h"

void	ft_putud(unsigned int n, int *d)
{
	if (n >= 10)
	{
		ft_putud((n / 10), d);
		ft_putud((n % 10), d);
	}
	else
		ft_putchar((n + 48), d);
}
